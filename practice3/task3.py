import random

# Сума у секундах двох випадкових величин: а годин та b хвилин
a = random.randint(0, 23)  # Випадкова година
b = random.randint(0, 59)  # Випадкова хвилина
seconds_sum = a * 3600 + b * 60
print(f"Сума у секундах двох випадкових величин: {a} годин {b} хвилин = {seconds_sum} секунд")

# Різниця у сантиметрах двох випадкових дійсних величин: n метрів та m дециметрів
n = random.uniform(0.0, 10.0)  # Випадкове число від 0 до 10
m = random.uniform(0.0, 9.9)  # Випадкове число від 0 до 9.9
centimeters_difference = (n * 100) - (m * 10)
print(f"Різниця у сантиметрах двох випадкових величин: {n} метрів {m} дециметрів = {centimeters_difference} см")

# Площа прямокутної ділянки зі сторонами a=N та b - випадкова величина від 1 до 100
N = random.randint(1, 100)  # Випадкова величина від 1 до 100
b = random.randint(1, 100)  # Випадкова величина від 1 до 100
rectangle_area = N * b
print(f"Площа прямокутної ділянки зі сторонами {N} та {b} = {rectangle_area} кв. одиниць")

# Піднесення до кубу двох випадкових цілих чисел
random_int1 = random.randint(-100, 100)
random_int2 = random.randint(-100, 100)
cube_result1 = random_int1 ** 3
cube_result2 = random_int2 ** 3
print(f"Піднесення до кубу двох випадкових цілих чисел: {random_int1}^3 = {cube_result1}, {random_int2}^3 = {cube_result2}")

# Остача від оплати ста гривнями за випадкову ціну купленого товару між 5 грн та 70 грн
price = random.uniform(5.0, 70.0)  # Випадкова ціна купленого товару
change = 100 % price
print(f"Остача від оплати ста гривнями за товар ціною {price} грн: {change} грн")
